class User {
  final String name;
  final String email;
  final String password;
  final String phoneNumber;
  final String address;
  final String profilePicture;
  final String bio;
  final String id;
  User({
    required this.name,
    required this.email,
    required this.password,
    required this.phoneNumber,
    required this.address,
    required this.profilePicture,
    required this.bio,
    required this.id,
  });
}